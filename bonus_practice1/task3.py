# Объяснение "сломанного интерпретатора"


# 0.3 != 0.9 --> False
print(0.3 == 0.9)


# 0.6 != 0.9 --> False
print(0.6 == 0.9)


# 0.8999999999999999
print(0.6 + 0.3)


# 0.8999999999999999 != 0.9 --> False
print(0.6 + 0.3 == 0.9)

# even if we put 0.6+0.3 in ()  0.8999999999999999 != 0.9 --> False
print((0.6 + 0.3) == 0.9)